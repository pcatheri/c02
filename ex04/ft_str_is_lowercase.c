#include<unistd.h>

int ft_str_is_lowercase(char *str)
{
	char n;
   
	n = 0;
	while (str[n] != '\0')
	{
		if (!(str[n] >= '0' && str[n] <= '9'))
		{
			return (0);
		}
		n++;
	}
	return (1);
}
